    private  void info_ventas(){
        DefaultCategoryDataset datos = new DefaultCategoryDataset();
        
        try{       
            CallableStatement procedimiento = this.cnx.prepareCall("{call PRODUCTO_VENTAS}");
            ResultSet rs = procedimiento.executeQuery();
              
            while(rs.next()){
                    datos.addValue(Integer.parseInt(rs.getString(4)), "Grafica 1",rs.getString(2) );
                }
        rs.close();
        }catch(Exception e){
                System.out.println("error");
                
        }
        StandardChartTheme theme = new StandardChartTheme("JFree/Shadow", true);
        Color color = new Color(173, 216, 230);
        theme.setPlotBackgroundPaint(color);
        theme.setChartBackgroundPaint(color.brighter());
        ChartFactory.setChartTheme(theme);        
        
        JFreeChart grafico = ChartFactory.createBarChart("Compras por producto", "Producto", "Cantidad", datos, PlotOrientation.VERTICAL, true, true, false);
        grafico.setBackgroundPaint(new Color(173, 216, 230));
        ChartPanel cPanel = new ChartPanel(grafico);
        cPanel.setBounds(0,10, 500, 400);
        jp_pagina_indicadores.add(cPanel);
        
       
    }
