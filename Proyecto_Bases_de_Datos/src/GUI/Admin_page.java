    void eliminar_registro(String sql,String sql2, JTable tabla, int columna, int columnas_tabla) throws SQLException{
        int fila = tabla.getSelectedRow();
        if(fila!=-1){
            PreparedStatement sentencia= cnx.prepareStatement(sql);
            sentencia.setInt(1,(int)tabla.getValueAt(fila, columna));
            sentencia.executeUpdate();
            JOptionPane.showMessageDialog(null, "Acción realizada con exito.");
            
            DefaultTableModel model =(DefaultTableModel) tabla.getModel();
            model.setRowCount(0);
            llenar_tabla(columnas_tabla,tabla,sql2);
        }else{
            JOptionPane.showMessageDialog(null, "Por favor seleccione una fila.");
        }

        
    }
    
    
   
