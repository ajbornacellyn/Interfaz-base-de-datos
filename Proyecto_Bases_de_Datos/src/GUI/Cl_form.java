            String sql = "CALL INSERTAR_CLIENTE (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
            
            PreparedStatement sentencia= cnx.prepareStatement(sql);
            sentencia.setString(1, Tf_nombre_comercial.getText());
            sentencia.setString(2, Tf_canal.getText());
            sentencia.setString(3, Tf_web.getText());
            sentencia.setString(4, Tf_tiempo.getText());
