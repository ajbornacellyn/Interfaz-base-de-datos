            sentencia.setString(8, Tf_actividad_principal.getText());
            sentencia.setString(9, Tf_intro.getText());
            sentencia.setString(10, Tfproveedor_principal.getText());
            sentencia.setInt(11, Integer.parseInt (Tfindicativo.getText()));
            sentencia.setString(12, Tfcel.getText());
            sentencia.setString(13, Tfpais.getText());
            sentencia.setString(14, Tfdepartamento.getText());
            sentencia.setString(15, Tfciudad.getText());
            sentencia.setString(16, Tfbarrio.getText());
            sentencia.setString(17, Tfcalle.getText());
            sentencia.setInt(18, Integer.parseInt (Tfcapacidad.getText()));
            sentencia.setString(19,Tflinea.getText());
            sentencia.setString(20,Tfmarca.getText());
            sentencia.setInt(21,Integer.parseInt (Tfsucursales.getText()));
            
            sentencia.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Cliente"+Tf_nombre_comercial.getText()+" creado con exito.");
            Admin_page ap= new Admin_page(cnx);
            DefaultTableModel model =(DefaultTableModel) t_clientes.getModel();
            model.setRowCount(0);
            ap.llenar_tabla(22,t_clientes,"CALL SHOW_CLIENTE()");
            
