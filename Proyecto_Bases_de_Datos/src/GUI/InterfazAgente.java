Boolean verificar_cliente_consolidado(){
        
        Boolean cliente = false;
        
        int fila = jTClientes1.getSelectedRow();
        
        if(fila!= -1){
          try{                
                CallableStatement pas = control.Main.getCrearConexion().getConexion().prepareCall("{call Cliente_consolidado(?)}");
                pas.setInt(1,(int) jTClientes1.getValueAt(fila, 0));
                ResultSet rs = pas.executeQuery();
                
                while(rs.next()){
                    cliente = true;       
                }
      
            rs.close();
            }catch(Exception e){
            System.out.println("error");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Porfavor seleccione una fila.");
        }
          
        return cliente;
          
        
    }
    
    void llenar_tabla(int columnas, javax.swing.JTable tabla, String sql){
    
        try{
            Statement st= control.Main.getCrearConexion().getConexion().createStatement();
            ResultSet rs=st.executeQuery(sql);
            DefaultTableModel modelo = (DefaultTableModel)tabla.getModel();
            while(rs.next()){
                Object [] fila = new Object[columnas]; 
                for (int i =0;i<columnas;i++){
                    fila[i]= rs.getObject(i+1);     
                }
                modelo.addRow(fila);
            }
            tabla.setModel(modelo);

        }catch(Exception e){
            System.out.println("ERROR AL CARGAR DATOS");
        }
    }
    
