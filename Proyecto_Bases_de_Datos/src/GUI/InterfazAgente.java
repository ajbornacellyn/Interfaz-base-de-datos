    private void labelAgregarPreguntaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelAgregarPreguntaMouseClicked
        if(!this.verificarHora(textLlamadaHora.getText()) || !this.verificarHora(textLlamadaTiempoE.getText()) ||
                !this.verificarHora(textLlamadaTiempoConv.getText())){
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Error.\nLos datos introducidos en la hora no son validos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            Statement s = control.Main.getCrearConexion().getConexion().createStatement();
            ResultSet rs = s.executeQuery ("SELECT getIDAgente()");
            int id = 0;
            while(rs.next()){
                id = rs.getInt(1);
            }
            
            SimpleDateFormat dFormat = new SimpleDateFormat("yyyy-MM-dd");
            String dateLlamada = dFormat.format(selecFechaLlamada.getDate());
            dateLlamada += " " + textLlamadaHora.getText();

            CallableStatement stmt = control.Main.getCrearConexion().getConexion().
            prepareCall("{CALL INSERT_Pregunta(?,?,?,?,?,?,?,?,?,?,?,?,?)}");
            stmt.setInt(1, Integer.parseInt(textLlamadaIDCliente.getText()));
            stmt.setInt(2, id);
            stmt.setString(3, dateLlamada);
            stmt.setString(4, textLlamadaTiempoE.getText());
            stmt.setString(5, llamadaServicio.getSelectedItem().toString());
            stmt.setString(6, llamadaEstado.getSelectedItem().toString());
            stmt.setString(7, textLlamadaTiempoConv.getText());
            stmt.setString(8, textLlamadaSatisfC.getText());
            stmt.setString(9, llamadaResolucion.getSelectedItem().toString());
            stmt.setString(10, textDetalle.getText());
            stmt.setString(11, textPreguntaCanal.getText());
            stmt.setString(12, llamadaEstado.getSelectedItem().toString());
            stmt.setString(13, dateLlamada);

            stmt.execute();

            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Pregunta Agregada Correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            textLlamadaHora.setText("");
            textLlamadaIDCliente.setText("");
            textLlamadaTiempoE.setText("");
            textLlamadaTiempoConv.setText("");
            textLlamadaSatisfC.setText("");
            textDetalle.setText("");
            textPreguntaCanal.setText("");
        }catch(Exception e){
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Se ha producido un error.\nRevisa los datos e intentalo de nuevo", "Error", JOptionPane.ERROR_MESSAGE);
            System.out.println(e);
        }

    }//GEN-LAST:event_labelAgregarPreguntaMouseClicked

