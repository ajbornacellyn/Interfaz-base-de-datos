    private void iniciarSesion(){
        control.Main.getCrearConexion().setUser(fieldUser.getText());
        control.Main.getCrearConexion().setPassword(String.valueOf(fieldPassword.getPassword()));
        control.Main.getCrearConexion().setHost("localhost");
        control.Main.getCrearConexion().createServer();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            control.Main.getCrearConexion().setConexion(DriverManager.getConnection
                (control.Main.getCrearConexion().getServer(), control.Main.getCrearConexion().getUser(),
                control.Main.getCrearConexion().getPassword()));
            switch (getRole()) {
                case "agente_call_center":
                    InterfazAgente interfAgente1 = new InterfazAgente();
                    interfAgente1.setVisible(true);
                    break;
                case "administrador_db":
                    Admin_page interadmin=new Admin_page(control.Main.getCrearConexion().getConexion());
                    interadmin.setVisible(true);
                    System.out.println("Admin");
                    break;
                case "cliente":
                    InterfazCliente interfCl1 = new InterfazCliente();
                    interfCl1.setVisible(true);
                    break;
                default:
                    break;
            }
            
            this.dispose();

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Error al cargar el Driver",
                "Error", JOptionPane.ERROR_MESSAGE);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al iniciar sesion. Vuelva a intentarlo.\n"
                + "Si el error persiste contacte al administrador de la base de datos",
                "Error", JOptionPane.ERROR_MESSAGE);
            this.fieldPassword.setText("");
        }
    }
    
