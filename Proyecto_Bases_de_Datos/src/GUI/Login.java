    private String getRole(){
        String rol = null;
        try{
            Statement s = control.Main.getCrearConexion().getConexion().createStatement();
            ResultSet rs = s.executeQuery ("SELECT CURRENT_ROLE()");
            while(rs.next()){
                String[] aux = rs.getString("CURRENT_ROLE()").split("@");
                rol = aux[0]; 
            }
        }catch (SQLException ex){
            System.out.println("Error");
        }
        
        return rol.substring(1, rol.length()-1);
    }
